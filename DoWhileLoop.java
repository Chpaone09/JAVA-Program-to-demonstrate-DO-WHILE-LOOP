public class DoWhileLoop{
    public static void main(String[] args) {
        int i =1;
        System.out.print("\n\n\t Demonstration of DO-WHILE LOOP in JAVA >> ");
        do{
            System.out.print(" "+(i++));
        }while(i<=5);
    }
    }
    
    